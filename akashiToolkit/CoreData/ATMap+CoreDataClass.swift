import Foundation
import CoreData

@objc(ATMap)
public class ATMap: NSManagedObject {
    
    /*
     id
     numId 海域内编号
     name 地图名称 例: "カ号迎撃戦"
     level: 难度
     infoText: html格式简介 例: "カムラン半島付近に<br>遊弋する敵艦隊を捕捉、<br>これを迎撃せよ！"
     maxHp: 海域HP最大值（没有HP则为0）
     requireDefeatCount: 攻略必要次数（无则为0）
     gain: 获得资源
     owner: 所属海域
     cells: 地图点列表
     */

}
